// Penjumlahan
const add = (num1, num2) => {
  return num1 + num2;
}

// Pengurangan
const subtract = (num1, num2) => {
  return num1 - num2;
}
  
// Perkalian
const multiply = (num1, num2) => {
  return num1 * num2;
}
    
// Pembagian
const divide = (num1, num2) => {
  return num1 / num2;
}

module.exports = {
  add,
  subtract,
  multiply,
  divide,
}
